triangles = [[[0,0,-1],[0,1,2],[1,0,4]],
            [[0,1,2],[1,0,4],[1,1,0]],
            [[1,1,0],[1,0,4],[2,0,2]],
            [[1,1,0],[2,0,2],[2,1,-2]],
            [[0,1,2],[1,1,0],[0,2,3]],
            [[0,2,3],[1,1,0],[1,2,4]],
            [[1,2,4],[1,1,0],[2,1,-2]],
            [[1,2,4],[2,1,-2],[2,2,3]]
]


count = 0
for T in triangles:
    count += 1
    x1,x2,x3 = T[0][0],T[1][0],T[2][0]
    y1,y2,y3 = T[0][1],T[1][1],T[2][1]
    f1,f2,f3 = T[0][2],T[1][2],T[2][2]

    delta = (x2 - x1) * (y3 - y1) - (x3 - x1) * (y2 - y1)

    a = ((x2 * y3 - x3 * y2) * f1 - (x1 * y3 - x3 * y1) * f2 + (x1 * y2 - x2 * y1) * f3) / delta
    b = ((f2 - f1) * (y3 - y1) - (f3 - f1) * (y2 - y1)) / delta
    c = ((f3 - f1) * (x2 - x1) - (f2 - f1) * (x3 - x1)) / delta

    print("T",count, "has a =",a,"b =",b,"c =",c)